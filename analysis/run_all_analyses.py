def _rq1_section(t: AnalysisTables) -> dict[str, Any]:
    chunks = build_chunk_frame(t)
    merges = build_merge_frame(t)
    prs = build_pr_frame(t)

    out: dict[str, Any] = {}

    if not merges.empty and "n_chunks" in merges.columns:
        # Among ALL merges (including no-conflict ones).
        out["chunks_per_merge_all_desc"] = _series_to_dict(
            descriptive_table(merges["n_chunks"])
        )
        # Among merges that produced at least one conflict chunk.
        conflicting = merges[merges["has_conflict"]]
        if not conflicting.empty:
            out["chunks_per_merge_conflicting_desc"] = _series_to_dict(
                descriptive_table(conflicting["n_chunks"])
            )

    # LOC stats per side (V1 / V2 / Base / Resolution).
    if not chunks.empty:
        for loc_col in ("v1_loc", "v2_loc", "base_loc", "resolution_loc"):
            if loc_col in chunks.columns:
                out[f"{loc_col}_desc"] = _series_to_dict(
                    descriptive_table(chunks[loc_col].dropna())
                )

        # Localization success rate.
        if "localized_ok" in chunks.columns:
            total = int(chunks.shape[0])
            ok = int(chunks["localized_ok"].sum())
            out["localized_ok"] = {
                "ok": ok,
                "total": total,
                "pct": _pct(ok, total),
            }

        # Decomposition of the Imprecise bucket into its two distinct sources:
        #   (a) localization_failures: LocalizeResRegion could not anchor the
        #       resolution region -> resolution is None -> strategy_raw = "Imprecise"
        #   (b) postponed: localization succeeded but the resolved file still
        #       contained conflict markers (<<<<<<<, =======, >>>>>>>) ->
        #       strategy_raw = "Postponed" -> folded into canonical "Imprecise"
        # This decomposition is needed because the two phenomena have different
        # methodological implications.
        if "strategy_raw" in chunks.columns and "localized_ok" in chunks.columns:
            n_loc_fail = int((~chunks["localized_ok"]).sum())
            n_postponed = int((chunks["strategy_raw"] == "Postponed").sum())
            out["imprecise_decomposition"] = {
                "localization_failures": n_loc_fail,
                "postponed": n_postponed,
                "total_imprecise": n_loc_fail + n_postponed,
                "note": (
                    "localization_failures = chunks where LocalizeResRegion returned None; "
                    "postponed = chunks where localization succeeded but resolved file "
                    "still contained conflict markers"
                ),
            }

    # PR outcome breakdown (resolved / abandoned / open-with-conflict).
    if not prs.empty and "pr_outcome" in prs.columns:
        out["pr_outcome"] = prs["pr_outcome"].value_counts().to_dict()

    # Conflict incidence per stratum.
    if not prs.empty and "has_conflict" in prs.columns:
        incidence: dict[str, dict[str, float]] = {}
        for axis_col, label in (
            ("agent", "per_agent"),
            ("language_top", "per_language_top"),
            ("pr_task_type", "per_task_type"),
        ):
            if axis_col not in prs.columns:
                continue
            grp = prs.groupby(axis_col)["has_conflict"].agg(["sum", "count"])
            incidence[label] = {
                str(k): {
                    "n_prs": int(row["count"]),
                    "n_with_conflict": int(row["sum"]),
                    "pct_with_conflict": _pct(row["sum"], row["count"]),
                }
                for k, row in grp.iterrows()
            }
        out["conflict_incidence"] = incidence

    return out


