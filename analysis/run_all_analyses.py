def _safe_len(df: pd.DataFrame) -> int:
    return int(len(df)) if df is not None else 0


def _series_to_dict(s: pd.Series) -> dict[str, Any]:
    return {str(k): (float(v) if pd.notna(v) else None) for k, v in s.items()}


def _df_to_records(df: pd.DataFrame) -> list[dict[str, Any]]:
    if df is None or df.empty:
        return []
    out = df.reset_index().to_dict(orient="records")
    # JSON-safe: convert numpy scalars, NaN -> None.
    cleaned = []
    for row in out:
        cleaned.append({
            k: (None if (isinstance(v, float) and pd.isna(v)) else
                (float(v) if hasattr(v, "item") and isinstance(v, (pd.Int64Dtype, pd.Float64Dtype))
                 else v.item() if hasattr(v, "item") else v))
            for k, v in row.items()
        })
    return cleaned


def _pct(num: float, denom: float) -> float:
    if denom <= 0:
        return 0.0
    return 100.0 * num / denom


