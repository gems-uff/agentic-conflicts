def _render_txt(summary: dict[str, Any]) -> str:
    lines: list[str] = []

    def header(txt: str) -> None:
        lines.append("")
        lines.append("=" * len(txt))
        lines.append(txt)
        lines.append("=" * len(txt))

    def subhead(txt: str) -> None:
        lines.append("")
        lines.append(txt)
        lines.append("-" * len(txt))

    def kv(label: str, value: Any) -> None:
        if isinstance(value, float):
            lines.append(f"  {label}: {value:,.4f}")
        elif isinstance(value, int):
            lines.append(f"  {label}: {value:,}")
        else:
            lines.append(f"  {label}: {value}")

    def dump_dict(d: dict[str, Any], indent: int = 2) -> None:
        pad = " " * indent
        for k, v in d.items():
            if isinstance(v, dict):
                lines.append(f"{pad}{k}:")
                dump_dict(v, indent + 2)
            elif isinstance(v, float):
                lines.append(f"{pad}{k}: {v:,.4f}")
            elif isinstance(v, int):
                lines.append(f"{pad}{k}: {v:,}")
            else:
                lines.append(f"{pad}{k}: {v}")

    header("DATASET CHARACTERISATION")
    dump_dict(summary.get("characterisation", {}))

    header("RQ1 -- STRUCTURAL NATURE")
