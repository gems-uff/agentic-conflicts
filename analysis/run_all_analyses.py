def _raw_aidev_universe_stats() -> dict[str, Any]:
    """Counts from the raw AIDev parquets (if present alongside ``data/raw/``)."""
    # Look for the AIDev raw drop in the two most common locations.
    candidates = [
        _ROOT / "data" / "raw" / "aidev" / "all_pull_request.parquet",
        _ROOT / "data" / "aidev" / "all_pull_request.parquet",
        _ROOT / "data" / "all_pull_request.parquet",
    ]
    pr_path = next((p for p in candidates if p.exists()), None)
    if pr_path is None:
        return {"note": "raw AIDev parquet not found; skipping raw-universe stats"}

    repo_path_candidates = [
        pr_path.parent / "all_repository.parquet",
        _ROOT / "data" / "raw" / "aidev" / "all_repository.parquet",
    ]
    repo_path = next((p for p in repo_path_candidates if p.exists()), None)

    out: dict[str, Any] = {"pr_parquet": str(pr_path)}
    try:
        prs = pd.read_parquet(pr_path, columns=[c for c in (
            "id", "repo_full_name", "full_name", "agent", "language", "state",
        ) if True])
    except Exception as exc:  # schema varies across AIDev drops
        prs = pd.read_parquet(pr_path)
        out["pr_read_note"] = f"full read (column selection failed: {exc})"

    out["aidev_total_prs"] = int(len(prs))
    repo_col = "repo_full_name" if "repo_full_name" in prs.columns else (
        "full_name" if "full_name" in prs.columns else None
    )
    if repo_col:
        out["aidev_total_repos"] = int(prs[repo_col].nunique())
    if "agent" in prs.columns:
        out["aidev_prs_per_agent"] = prs["agent"].value_counts().to_dict()
    if "language" in prs.columns:
        out["aidev_prs_per_language_top15"] = (
            prs["language"].fillna("Unknown").value_counts().head(15).to_dict()
        )
    if "state" in prs.columns:
        out["aidev_prs_per_state"] = prs["state"].value_counts().to_dict()

    if repo_path is not None:
        try:
            repos = pd.read_parquet(repo_path)
            out["aidev_total_repositories_table"] = int(len(repos))
        except Exception as exc:
            out["aidev_repositories_read_error"] = str(exc)

    return out


def _universe_stats(t_raw: AnalysisTables, t_dedup: AnalysisTables) -> dict[str, Any]:
    u_raw = t_raw.universe
    u_dedup = t_dedup.universe
    out: dict[str, Any] = {}
    if u_raw is None or u_raw.empty:
        return {"note": "universe parquet missing"}

    # PR-level counts (universe is keyed on (pr_id, sha); drop SHA dim for PR counts).
    prs_raw = u_raw.drop_duplicates(subset=["pr_id"]) if "pr_id" in u_raw.columns else u_raw
    prs_dedup = u_dedup.drop_duplicates(subset=["pr_id"]) if "pr_id" in u_dedup.columns else u_dedup

    out["universe_rows_raw"] = _safe_len(u_raw)
    out["universe_rows_dedup"] = _safe_len(u_dedup)
    out["universe_unique_prs_raw"] = _safe_len(prs_raw)
    out["universe_unique_prs_dedup"] = _safe_len(prs_dedup)

    for col, key in (("agent", "prs_per_agent"), ("language", "prs_per_language"),
                     ("pr_task_type", "prs_per_task_type"), ("state", "prs_per_state")):
        if col in prs_dedup.columns:
            out[key] = prs_dedup[col].fillna("Unknown").value_counts().to_dict()

    repo_col = "full_name" if "full_name" in prs_dedup.columns else (
        "repo_full_name" if "repo_full_name" in prs_dedup.columns else None
    )
    if repo_col:
        out["unique_repos"] = int(prs_dedup[repo_col].nunique())

    return out


def _merges_stats(t_raw: AnalysisTables, t_dedup: AnalysisTables) -> dict[str, Any]:
    im_raw = t_raw.internal_merges
    im_dedup = t_dedup.internal_merges
    out: dict[str, Any] = {
        "internal_merges_rows_raw": _safe_len(im_raw),
        "internal_merges_rows_dedup": _safe_len(im_dedup),
    }
    if im_raw is None or im_raw.empty:
        return out

    key_cols = [c for c in ("repo_full_name", "merge_sha") if c in im_raw.columns]
    if len(key_cols) == 2:
        out["unique_physical_merges"] = int(im_raw.drop_duplicates(subset=key_cols).shape[0])

    # PR fan-out: how many PRs reference each physical merge.
    if len(key_cols) == 2 and "pr_id" in im_raw.columns:
        fan = pr_fanout(im_raw)
        fan_desc = descriptive_table(fan["n_prs"])
        out["pr_fanout_desc"] = _series_to_dict(fan_desc)
        out["pr_fanout_hist"] = fan["n_prs"].value_counts().sort_index().to_dict()
        out["merges_with_multiple_prs"] = int((fan["n_prs"] > 1).sum())
        out["merges_with_multiple_prs_pct"] = _pct(
            (fan["n_prs"] > 1).sum(), fan.shape[0],
        )

    if "resolver_type" in im_dedup.columns:
        out["resolver_breakdown"] = (
            im_dedup["resolver_type"].fillna("Unknown").value_counts().to_dict()
        )

    return out


def _characterisation_section() -> dict[str, Any]:
    t_raw = load_tables(deduplicate=False)
    t_dedup = load_tables(deduplicate=True)

    chunks_raw = t_raw.classified_chunks
    chunks_dedup = t_dedup.classified_chunks
    resolved_raw = t_raw.resolved_chunks
    resolved_dedup = t_dedup.resolved_chunks

    section: dict[str, Any] = {
        "data_dir": str(DATA_DIR),
        "raw_aidev": _raw_aidev_universe_stats(),
        "universe": _universe_stats(t_raw, t_dedup),
        "internal_merges": _merges_stats(t_raw, t_dedup),
        "classified_chunks": {
            "rows_raw": _safe_len(chunks_raw),
            "rows_dedup": _safe_len(chunks_dedup),
        },
        "resolved_chunks": {
            "rows_raw": _safe_len(resolved_raw),
            "rows_dedup": _safe_len(resolved_dedup),
        },
        "extraction_errors": {
            "rows": _safe_len(t_dedup.extraction_errors),
        },
    }

    # Headline counts the paper quotes in the Dataset Overview.
    im_dedup = t_dedup.internal_merges
    conflicting_merges = 0
    if not im_dedup.empty and not chunks_dedup.empty:
        key = ["repo_full_name", "merge_sha"]
        conflicting_merges = int(
            chunks_dedup[key].drop_duplicates().shape[0]
        )
    # PR-level counts: PRs that have at least one internal merge and at least
    # one conflicting merge.  build_pr_frame joins the per-PR merge/chunk
    # aggregates onto the universe, so has_internal_merge and has_conflict are
    # already computed there.
    pr_frame = build_pr_frame(t_dedup)
    n_prs_with_internal_merge = int(pr_frame["has_internal_merge"].sum()) if not pr_frame.empty else 0
    n_prs_with_conflict = int(pr_frame["has_conflict"].sum()) if not pr_frame.empty else 0
    n_prs_total = int(pr_frame.shape[0]) if not pr_frame.empty else 1  # avoid division by zero

    section["headline"] = {
        "n_projects": section["universe"].get("unique_repos"),
        "n_prs": section["universe"].get("universe_unique_prs_dedup"),
        "n_prs_with_internal_merge": n_prs_with_internal_merge,
        "pct_prs_with_internal_merge": _pct(n_prs_with_internal_merge, n_prs_total),
        "n_prs_with_conflicting_merge": n_prs_with_conflict,
        "pct_prs_with_conflicting_merge": _pct(n_prs_with_conflict, n_prs_total),
        "n_internal_merges": _safe_len(im_dedup),
        "n_conflicting_merges": conflicting_merges,
        "n_conflict_chunks": _safe_len(chunks_dedup),
        "conflicting_merges_pct": _pct(conflicting_merges, _safe_len(im_dedup)),
    }

    # Strata on the merge frame -- useful for the paper's "per-agent /
    # per-language / per-task-type" one-liners in the Dataset Overview.
    merge_frame = build_merge_frame(t_dedup)
    for axis_col, label in (
        ("agent", "merges_per_agent"),
        ("language_top", "merges_per_language_top"),
        ("pr_task_type", "merges_per_task_type"),
    ):
        if axis_col in merge_frame.columns:
            vc = merge_frame[axis_col].fillna("Unknown").value_counts()
            section["headline"][label] = vc.to_dict()
            section["headline"][label + "_conflict_rate"] = {
                str(k): _pct(
                    int(merge_frame[
                        (merge_frame[axis_col] == k) & merge_frame["has_conflict"]
                    ].shape[0]),
                    int(vc.get(k, 0)),
                )
                for k in vc.index
            }

    return section


