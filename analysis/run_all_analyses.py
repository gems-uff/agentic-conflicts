def _rq3_section(t: AnalysisTables) -> dict[str, Any]:
    chunks = build_chunk_frame(t)
    merges = build_merge_frame(t)

    out: dict[str, Any] = {}

    if not merges.empty and "resolver_type" in merges.columns:
        vc = merges["resolver_type"].fillna("Unknown").value_counts()
        out["resolver_counts_merge_level"] = vc.to_dict()
        out["resolver_share_merge_level"] = (vc / vc.sum()).to_dict()

    if not chunks.empty and "resolver_type" in chunks.columns:
        # Agent vs human chunk-level strategy shares, Imprecise excluded (paper convention).
        sub = chunks[chunks["resolver_type"].isin(["agent", "human"])]
        if not sub.empty:
            dist_excl = strategy_distribution(sub, group_col="resolver_type", exclude_imprecise=True)
            dist_incl = strategy_distribution(sub, group_col="resolver_type", exclude_imprecise=False)
            out["strategy_by_resolver_excl_imprecise"] = {
                str(k): _series_to_dict(row) for k, row in dist_excl.iterrows()
            }
            out["strategy_by_resolver_incl_imprecise"] = {
                str(k): _series_to_dict(row) for k, row in dist_incl.iterrows()
            }
            out["imprecise_share_by_resolver"] = imprecise_share(
                sub, group_col="resolver_type"
            ).to_dict()

    # Resolver x agent cross-tab -- who resolves the conflicts that agent-opened PRs raise?
    if not merges.empty and {"agent", "resolver_type"}.issubset(merges.columns):
        ct = pd.crosstab(
            merges["agent"].fillna("Unknown"),
            merges["resolver_type"].fillna("Unknown"),
        )
        out["resolver_by_agent_counts"] = {
            str(idx): row.to_dict() for idx, row in ct.iterrows()
        }
        ct_pct = ct.div(ct.sum(axis=1), axis=0)
        out["resolver_by_agent_shares"] = {
            str(idx): _series_to_dict(row) for idx, row in ct_pct.iterrows()
        }

    return out


