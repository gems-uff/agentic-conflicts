"""One-shot script to produce every number the paper's Results section needs.

Why this exists
---------------
The four analysis notebooks (dataset_characterization, rq1_structural_nature,
rq2_resolution_strategies, rq3_resolver) each print or plot the same core
aggregates we ultimately want in the paper. This script calls the shared
helpers in ``analysis.common`` once, rolls up everything to plain dict/JSON,
and writes two files in ``analysis/results/``:

* ``results_summary.json`` -- machine-readable, full precision;
* ``results_summary.txt``  -- flat, human-readable dump for quick inspection.

How to run
----------
From the project root, with the Python environment that has pandas / pyarrow::

    python -m analysis.run_all_analyses

or::

    python analysis/run_all_analyses.py

The script does NOT execute the notebooks (no matplotlib / no figures). Its
only job is to produce the numbers. Figures are still generated by the
notebooks themselves.

What it reports
---------------
1.  Dataset characterisation (raw AIDev universe + our conflict subset).
2.  RQ1 -- structural profile: chunks-per-merge distribution, LOC stats,
    conflict incidence per stratum.
3.  RQ2 -- resolution strategies: distribution with and without the
    Imprecise bucket, plus the Imprecise share per stratum.
4.  RQ3 -- resolver split (agent vs human) and strategy differences.

The script is deliberately defensive: every section is wrapped so a missing
table does not break the others.
"""

